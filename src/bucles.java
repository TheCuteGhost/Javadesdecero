public class bucles {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("El valor de i es: " + i);
        }
        System.out.println( "Fin del programa");

        int x = 0;
        while (x < 10) {
            System.out.println("El valor de x es: " + x);
            x++;
        }
        System.out.println( "Fin del programa");
    }
}
